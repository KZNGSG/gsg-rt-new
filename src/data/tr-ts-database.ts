export const ALL_REGULATIONS: TRTSRegulation[] = [
  ...CERTIFICATE_REGULATIONS,
  ...DECLARATION_REGULATIONS,
];

// =============================================================================
// ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
// =============================================================================

/**
 * Получить регламент по slug
 */
export function getRegulationBySlug(slug: string): TRTSRegulation | undefined {
  return ALL_REGULATIONS.find(r => r.slug === slug);
}

/**
 * Получить регламент по номеру (например, "007-2011")
 */
export function getRegulationById(id: string): TRTSRegulation | undefined {
  return ALL_REGULATIONS.find(r => r.id === id);
}

/**
 * Получить все сертификаты
 */
export function getCertificateRegulations(): TRTSRegulation[] {
  return CERTIFICATE_REGULATIONS;
}

/**
 * Получить все декларации
 */
export function getDeclarationRegulations(): TRTSRegulation[] {
  return DECLARATION_REGULATIONS;
}

/**
 * Получить регламенты по категории
 */
export function getRegulationsByCategory(category: TRTSRegulation['category']): TRTSRegulation[] {
  return ALL_REGULATIONS.filter(r => r.category === category);
}

/**
 * Получить все slug'и для generateStaticParams
 */
export function getAllCertificateSlugs(): string[] {
  return CERTIFICATE_REGULATIONS.map(r => r.slug);
}

export function getAllDeclarationSlugs(): string[] {
  return DECLARATION_REGULATIONS.map(r => r.slug);
}

/**
 * Поиск по ключевым словам
 */
export function searchRegulations(query: string): TRTSRegulation[] {
  const q = query.toLowerCase();
  return ALL_REGULATIONS.filter(r =>
    r.shortName.toLowerCase().includes(q) ||
    r.name.toLowerCase().includes(q) ||
    r.content.products.some(p => p.toLowerCase().includes(q)) ||
    r.seo.keywords.some(k => k.toLowerCase().includes(q))
  );
}
