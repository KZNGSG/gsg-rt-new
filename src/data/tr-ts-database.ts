export type DocumentType = 'certificate' | 'declaration' | 'sgr';

export interface TRTSPricing {
  serial: { price: number; priceMax: number; days: string };   // Серийное производство
  batch: { price: number; priceMax: number; days: string };    // Партия
  urgent?: { price: number; priceMax: number; days: string };  // Срочное
}

export interface TRTSFAQ {
  question: string;
  answer: string;
}

export interface TRTSRegulation {
  // Идентификация
  id: string;                    // "007-2011"
  number: string;                // "ТР ТС 007/2011"
  fullNumber: string;            // "Технический регламент Таможенного союза 007/2011"

  // URL и названия
  slug: string;                  // "007-detskie-tovary"
  name: string;                  // "О безопасности продукции, предназначенной для детей и подростков"
  shortName: string;             // "Детские товары"

  // Какой документ нужен
  documentType: DocumentType;

  // Для страниц где нужно и то и другое (разные товары)
  hasAlternativeDoc?: {
    type: DocumentType;
    description: string;         // "Для машин и оборудования по схеме 1д"
  };

  // СГР для отдельных категорий
  sgrRequired?: {
    categories: string[];        // ["Детская косметика", "БАДы"]
    description: string;
  };

  // SEO метаданные
  seo: {
    title: string;               // Title для страницы
    description: string;         // Meta description
    keywords: string[];          // Ключевые слова
    h1: string;                  // H1 заголовок
  };

  // Контент страницы
  content: {
    intro: string;               // Вступительный текст
    products: string[];          // Какие товары подлежат
    excluded?: string[];         // Что НЕ подлежит
    documents: string[];         // Необходимые документы
    process: string[];           // Этапы оформления
  };

  // Цены и сроки
  pricing: TRTSPricing;

  // Схемы подтверждения соответствия
  schemes: string[];             // ["1с", "2с", "3с", "4с"]

  // Срок действия документа
  validity: {
    serial: string;              // "до 5 лет"
    batch: string;               // "на партию"
  };

  // FAQ
  faq: TRTSFAQ[];

  // Связи
  relatedTRTS: string[];         // Связанные регламенты ["020-2011"]
  tnvedCodes: string[];          // Коды ТН ВЭД ["9503", "9504"]

  // Категория для фильтрации
  category: 'children' | 'food' | 'electronics' | 'machinery' | 'transport' | 'clothing' | 'cosmetics' | 'furniture' | 'other';

  // Активность
  isActive: boolean;             // Действует ли регламент
  effectiveDate: string;         // Дата вступления в силу
}

